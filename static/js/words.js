/**
 * Return (or create) a shuffled column order for a speaker.
 * Stored in localStorage as "colOrder_{lang}_{speaker}".
 * Number of columns = getLangConfig(lang).numSubsections.
 */
function getColOrder(lang, speaker) {
  const { numSubsections } = getLangConfig(lang);
  const key    = `colOrder_${lang}_${speaker}`;
  const stored = localStorage.getItem(key);
  if (stored) {
    const parsed = JSON.parse(stored);
    if (parsed.length === numSubsections) return parsed;
  }
  const order = Array.from({ length: numSubsections }, (_, i) => i + 1);
  for (let i = order.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [order[i], order[j]] = [order[j], order[i]];
  }
  localStorage.setItem(key, JSON.stringify(order));
  return order;
}

/**
 * Return word_ids for a subsession.
 * Number of words = getLangConfig(lang).numSets.
 * Word_id format: "set01_col07"  (1-indexed in both parts)
 *
 * If `randomColumn` is true, each row picks an independent random column
 * (one word per row, but column varies per row). Otherwise all rows share
 * the column from getColOrder()[subsection-1] (original fixed-column mode).
 */
function getSessionWords(subsection, lang = "en", speaker = "", randomColumn = false) {
  const { numSets, numSubsections } = getLangConfig(lang);
  if (subsection < 1 || subsection > numSubsections) {
    throw new RangeError(`subsection must be 1–${numSubsections}`);
  }
  const fixedCol = getColOrder(lang, speaker)[subsection - 1];
  const rows = Array.from({ length: numSets }, (_, i) => i + 1);
  for (let i = rows.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [rows[i], rows[j]] = [rows[j], rows[i]];
  }
  return rows.map(rowNum => {
    const col = randomColumn
      ? Math.floor(Math.random() * numSubsections) + 1
      : fixedCol;
    return `set${String(rowNum).padStart(2, "0")}_col${String(col).padStart(2, "0")}`;
  });
}

/**
 * Look up the canonical word for a word_id like "set01_col03".
 * For Japanese entries stored as "kanji|reading", returns only the kanji part.
 */
function getCanonical(wordId, lang = "en") {
  const m = /^set(\d+)_col(\d+)$/.exec(wordId);
  if (!m) return wordId;
  const row  = parseInt(m[1], 10) - 1;
  const col  = parseInt(m[2], 10) - 1;
  const data = WORD_DATA[lang] || WORD_DATA.en;
  const word = (data[row] && data[row][col]) ?? wordId;
  return typeof word === "string" ? word.split("|")[0] : word;
}

/**
 * Return { main, reading } for display. reading is null for non-Japanese words.
 * For Japanese entries stored as "kanji|reading", returns both parts.
 */
function getWordParts(wordId, lang = "en") {
  const m = /^set(\d+)_col(\d+)$/.exec(wordId);
  if (!m) return { main: wordId, reading: null };
  const row  = parseInt(m[1], 10) - 1;
  const col  = parseInt(m[2], 10) - 1;
  const data = WORD_DATA[lang] || WORD_DATA.en;
  const word = (data[row] && data[row][col]) ?? wordId;
  if (typeof word === "string" && word.includes("|")) {
    const [main, reading] = word.split("|");
    return { main, reading };
  }
  return { main: typeof word === "string" ? word : wordId, reading: null };
}
